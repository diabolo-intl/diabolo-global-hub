window.handleContactSubmit = function (e) {
  e.preventDefault();
  const f = e.target;
  const name    = (f.name?.value || "").trim();
  const phone   = (f.phone?.value || "").trim();
  const email   = (f.email?.value || "").trim();
  const purpose = (f.purpose?.value || "").trim();
  const message = (f.message?.value || "").trim();

  const subject = `[IDF Website] ${purpose} — ${name}`;
  const body =
`Name: ${name}
Phone: ${phone}
Email: ${email}
Purpose: ${purpose}

${message}`;

  window.location.href =
    `mailto:diabolo.intl@gmail.com?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
  return false;
};

