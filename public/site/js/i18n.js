function navLink(href, key, isActive, mobile = false) {
  const base = mobile
    ? "block rounded-md px-3 py-2 text-sm font-medium"
    : "rounded-md px-3 py-2 text-sm font-medium";
  const cls = isActive
    ? `${base} bg-slate-100 text-slate-900`
    : `${base} text-slate-600 hover:text-slate-900 hover:bg-slate-50`;
  return `<a href="${href}" class="${cls}" data-i18n="${key}"></a>`;
}

function langToggle() {
  const opt = (code, label) => `
    <button type="button" data-lang-btn="${code}" onclick="setLang('${code}')"
      class="rounded-md px-2.5 py-1 text-xs font-medium text-slate-600 transition">
      ${label}
    </button>`;
  return `
    <div class="inline-flex items-center gap-0.5 rounded-lg border border-slate-200 bg-white p-0.5">
      ${opt("en", "EN")}
      ${opt("zh-CN", "简")}
      ${opt("zh-TW", "繁")}
    </div>`;
}

