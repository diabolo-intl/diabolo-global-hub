const LANG_KEY = "idf.lang";
const SUPPORTED = ["en", "zh-CN", "zh-TW"];
const DEFAULT_LANG = "en";

function getLang() {
  const stored = localStorage.getItem(LANG_KEY);
  if (stored && SUPPORTED.includes(stored)) return stored;
  const nav = (navigator.language || "").toLowerCase();
  if (nav.startsWith("zh")) return nav.includes("tw") || nav.includes("hk") ? "zh-TW" : "zh-CN";
  return DEFAULT_LANG;
}

function setLang(lang) {
  if (!SUPPORTED.includes(lang)) return;
  localStorage.setItem(LANG_KEY, lang);
  applyLang(lang);
}

function t(key, lang) {
  return (I18N[lang] && I18N[lang][key]) || I18N[DEFAULT_LANG][key] || key;
}

function applyLang(lang) {
  document.documentElement.lang = lang;

  // Text bindings
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n, lang);
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml, lang);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.setAttribute("placeholder", t(el.dataset.i18nPlaceholder, lang));
  });

  // Toggle UI state
  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    const active = btn.dataset.langBtn === lang;
    btn.classList.toggle("bg-slate-900", active);
    btn.classList.toggle("text-white", active);
    btn.classList.toggle("text-slate-600", !active);
  });

  // Page-specific renderers
  if (typeof window.renderMembers === "function") window.renderMembers(lang);
  if (typeof window.renderNews === "function") window.renderNews(lang);
  if (typeof window.renderTournaments === "function") window.renderTournaments(lang);
  if (typeof window.renderGradingEvents === "function") window.renderGradingEvents(lang);
}

